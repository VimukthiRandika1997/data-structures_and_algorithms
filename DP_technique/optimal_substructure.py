'''
What is called Optimal Substructure ?

- It means, that optimal solution to a problem of size n (having n elements) 
  is based on an optimal solution to the same problem of smaller size k ( k < n).

- While building the solution for a problem of size n, define it in terms of
  similar problems of smaller size k

'''


'''
- Coming up with a recursive formula or defining the optimal substructure
  is the first step for Dynamic Programming. 
  
- If we can not find a recursive formula for the problem, 
  we may not be thinking about using dynamic programming.

'''